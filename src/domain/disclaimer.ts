/** 출처/기준시점 표기 문자열 생성 */
export function 출처표기(source?: string, collectedAt?: string, url?: string): string {
  const parts: string[] = [];
  if (source) parts.push(`출처: ${source}`);
  if (collectedAt) parts.push(`수집시점: ${collectedAt}`);
  if (url) parts.push(`원문: ${url}`);
  return parts.length ? parts.join(" · ") : "출처: 확인 불가";
}

