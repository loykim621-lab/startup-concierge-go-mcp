/**
 * A. 창업 여부 판정 — 「중소기업창업 지원법 시행령」 제2조 + 참고3 창업여부 기준표.
 * 가장 자주 틀리는 함정: 기존 사업자라도 "이종(다른 업종) 창업"은 창업으로 인정된다.
 */
import type { Profile } from "./types.js";

export interface StartupStatus {
  창업: boolean;
  근거: string;
}

/** 동종 폐업 후 재창업 인정 경과기준: 3년(36개월) */
const 동종재창업_경과개월 = 36;

export function evaluateStartupStatus(p: Profile): StartupStatus {
  // 기존 사업자가 아니면(신규 등록) 명백한 창업
  if (!p.기존사업자) {
    return { 창업: true, 근거: "기존 사업자 보유 이력이 없는 신규 창업(시행령 제2조)." };
  }

  // 기존 사업자 유지 상태 — 이종/동종 판정
  const 기존 = p.기존업종_ksic5;
  const 신규 = p.신규업종_ksic5;

  if (기존 && 신규) {
    const 이종 = 기존 !== 신규; // KSIC 세세분류 5자리 불일치 = 이종
    if (이종) {
      return {
        창업: true,
        근거: `기존 업종(${기존})과 다른 이종 업종(${신규}) 창업 → 창업 인정(시행령 제2조, 참고3). 예: 음식점 유지 + 신규 SW플랫폼.`,
      };
    }
    // 동종(5자리 일치)
    if (p.폐업여부 && (p.폐업경과_개월 ?? 0) >= 동종재창업_경과개월) {
      return {
        창업: true,
        근거: `동종 업종이나 폐업 후 ${p.폐업경과_개월}개월(≥36개월) 경과 → 창업 인정.`,
      };
    }
    return {
      창업: false,
      근거: "동종(표준산업분류 세세분류 5자리 일치) 창업이며 폐업 3년 경과 등 조건 미충족 → 창업 아님.",
    };
  }

  // 업종 코드 정보가 부족 → 단정 금지
  return {
    창업: false,
    근거: "기존 사업자 보유 상태이나 기존/신규 업종 코드(KSIC 5자리)가 없어 이종 여부 확인 불가 → 보수적으로 창업 미인정(업종 코드 입력 시 재판정).",
  };
}
