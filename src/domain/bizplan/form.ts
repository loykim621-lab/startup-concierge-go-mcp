/** 필드 유형 — 서식 칸의 성격. */
export type 필드유형 = "표" | "서술" | "자금표" | "체크" | "기타";

/** 서식에서 뽑아낸 칸(필드) 하나. */
export interface FormField {
  /** 등장 순서(1부터). 필드 목록의 배열 순서와 일치(서식 순서 보존). */
  순번: number;
  /** 칸 이름(헤더/라벨 텍스트, 안내문·번호기호 제거 후). */
  칸이름: string;
  /** 칸 유형. */
  유형: 필드유형;
  /** 괄호 안내문·※ 안내문 등(있으면). 창업자가 무엇을 적어야 하는지 힌트. */
  안내문구?: string;
  /** PSST 섹션 매핑(칸이름이 PSST 섹션명이면). P·S1·S2·T. */
  psst매핑?: PsstKey;
  /** 이 칸에 들어갈 '사실'을 묻는 자연어 질문(required.ts 스타일). */
  질문: string;
}

/** 서식 분석 결과. */
export interface FormAnalysis {
  /** 선택 공고 id(있을 때만 — 사실을 지어내지 않고 그대로 반영). */
  grant_id?: string;
  /** 서식이 서식으로 보이지 않을 때의 정중한 재요청 메시지(있으면 나머지는 비어 있을 수 있음). */
  오류?: string;
  /** 서식 등장 순서 그대로의 필드 목록. */
  필드목록: FormField[];
  /** 감지 요약(유형별 개수·PSST 매핑 등 한 줄 요약). */
  감지요약: string;
  /** 창업자가 채워야 하는 사실 항목("[입력 필요: ___]"). */
  입력필요: string[];
  /** 작성 고지(이 산출물의 효력 한계·사실 무결성). */
  고지: string;
}

export interface FormAnalysisInput {
  /** 창업자가 붙여넣은 서식 텍스트. */
  form_text: string;
  /** 선택 공고 id(선택 — 결과에 그대로 반영). */
  grant_id?: string;
}

