/** 서식 최소 길이(이보다 짧으면 서식으로 보지 않음). */
const 최소길이 = 80;
/** 서식으로 인정할 최소 필드 수. */
const 최소필드수 = 2;

/** 원문자(①~⑨) → 아라비아 숫자(질문·정리용). */
const 원문자맵: Record<string, string> = {
  "①": "1", "②": "2", "③": "3", "④": "4", "⑤": "5",
  "⑥": "6", "⑦": "7", "⑧": "8", "⑨": "9",
};

/**
 * PSST 섹션명 → 키 매핑 규칙(결정적).
 * 칸이름에 아래 키워드가 포함되면 해당 PSST 섹션으로 매핑한다.
 * 순서 주의: 더 구체적인 규칙을 먼저 둔다(예 "실현가능성"이 "성장"보다 먼저일 필요는 없으나
 *   "성장전략"과 "실현가능성"은 서로 겹치지 않으므로 안전).
 */
const PSST_규칙: { 키워드: RegExp; key: PsstKey; 이름: string }[] = [
  { 키워드: /문제\s*인식|problem/i, key: "P", 이름: "문제인식" },
  { 키워드: /실현\s*가능성|solution/i, key: "S1", 이름: "실현가능성" },
  { 키워드: /성장\s*전략|scale[\s-]*up/i, key: "S2", 이름: "성장전략" },
  { 키워드: /팀\s*구성|팀\s*원|조직\s*구성|team/i, key: "T", 이름: "팀구성" },
];

/** 자금표(자금집행계획) 감지 키워드 — 있으면 유형 '자금표'. */
const 자금표_규칙 = /자금\s*집행|사업비|예산\s*(?:집행|계획)|비목|산출\s*근거|정부지원사업비/;

/** 표형 칸 감지 키워드 — 일반현황·팀구성표·ESG표 등. */
const 표_규칙 = /일반\s*현황|창업\s*아이템\s*개요|팀\s*구성(?:\s*표|\(안\))|협력\s*기관|경쟁(?:사|제품)\s*(?:비교|분석)|현황\s*표|개요\s*표/;

/** 서술형(본문) 감지 — PSST 섹션·필요성·계획 서술 등. */
const 서술_규칙 = /문제\s*인식|실현\s*가능성|성장\s*전략|필요성|아이템\s*소개|추진\s*(?:전략|계획)|개발\s*계획|사업화|시장|전략|방안/;

// ── 헬퍼(결정적) ─────────────────────────────────────────────────────────────

/** "[입력 필요: ___]" 표준 표기. */
function 입력필요표기(무엇: string): string {
  return `[입력 필요: ${무엇}]`;
}

/** 원문자를 아라비아 숫자로 치환(표기 정규화용). */
function 원문자정규화(s: string): string {
  return s.replace(/[①②③④⑤⑥⑦⑧⑨]/g, (m) => 원문자맵[m] ?? m);
}

/**
 * 한 줄에서 라벨(칸이름)과 안내문구를 분리한다.
 * - 선두 번호(1. / 1-1. / ①~⑨)·섹션 마커(□·■·▶·●·○)를 제거.
 * - 괄호 안내문 "(~을 기재)" / ※ 안내문을 안내문구로 뽑고 라벨에서 제거.
 * - 콜론(:)·탭·연속공백 뒤의 '빈 값'은 라벨 형식의 근거로만 쓰고 라벨엔 넣지 않는다.
 */
function 라벨분리(line: string): { 칸이름: string; 안내문구?: string } {
  let s = line.trim();

  // 선두 마커/번호 제거(□ ■ ▶ ● ○ - * · 및 1. / 1-1. / ①~⑨)
  s = s
    .replace(/^[\s]*[□■▶◆●○\-*·]+\s*/u, "")
    .replace(/^\s*\d+(?:-\d+)*\.\s*/, "")
    .replace(/^\s*\d+(?:-\d+)*\)\s*/, "")
    .replace(/^\s*[①②③④⑤⑥⑦⑧⑨]\s*/, "");

  // 안내문구 후보: 괄호() 부연 또는 ※ 안내
  const 안내: string[] = [];
  // ※ 안내(줄 어디든 — 보통 끝)
  const 별표 = s.match(/※[^\n]*$/);
  if (별표) {
    안내.push(별표[0].replace(/^※\s*/, "").trim());
    s = s.replace(/※[^\n]*$/, "").trim();
  }
  // 괄호 부연(가장 마지막 괄호쌍) — "(~기재)", "(예: ...)" 등
  const 괄호 = [...s.matchAll(/\(([^)]*)\)/g)];
  if (괄호.length > 0) {
    for (const g of 괄호) {
      const inner = g[1].trim();
      if (inner) 안내.push(inner);
    }
    s = s.replace(/\([^)]*\)/g, " ").trim();
  }

  // 라벨: 콜론/탭/연속공백 앞부분(값 칸은 비어 있으므로 라벨만 취함)
  // 콜론이 있으면 콜론 앞을 라벨로.
  const 콜론 = s.split(/\s*[:：]\s*/);
  let 칸이름 = 콜론[0];
  // 탭·연속공백(2칸+)으로 라벨:빈값이 표현된 경우 앞부분만
  칸이름 = 칸이름.split(/\t|\s{2,}/)[0];
  칸이름 = 칸이름.replace(/\s+/g, " ").trim();

  const 안내문구 = 안내.length > 0 ? 안내.join(" / ") : undefined;
  return 안내문구 ? { 칸이름, 안내문구 } : { 칸이름 };
}

/**
 * 한 줄이 '필드 헤더(칸)'로 보이는지 판정(결정적).
 * 다음 중 하나면 필드 후보:
 *  ① 번호 헤더: 1. / 1-1. / 1) / ①~⑨ 로 시작
 *  ② 섹션 마커: □ / ■ / ▶ / ● / ○ 로 시작
 *  ③ 표 라벨: '라벨:' 또는 '라벨\t' 또는 '라벨   '(연속공백) — 콜론/탭/2칸+ 뒤에 값이 비거나 짧음
 */
function 필드헤더인가(line: string): { hit: boolean; via: "번호" | "마커" | "라벨" | null } {
  const s = line.trim();
  if (!s) return { hit: false, via: null };

  // ① 번호 헤더
  if (/^\s*(?:\d+(?:-\d+)*[.)]|[①②③④⑤⑥⑦⑧⑨])\s*\S/.test(s)) {
    return { hit: true, via: "번호" };
  }
  // ② 섹션 마커
  if (/^\s*[□■▶◆●○]\s*\S/.test(s)) {
    return { hit: true, via: "마커" };
  }
  // ③ 표 라벨(라벨: / 라벨\t / 라벨  )
  //   - 콜론형: 앞에 라벨 텍스트가 있고 콜론이 있음
  if (/^[^:：\n]{1,40}[:：]\s*\S{0,}$/.test(s) && /[가-힣A-Za-z]/.test(s.split(/[:：]/)[0])) {
    return { hit: true, via: "라벨" };
  }
  //   - 탭/연속공백형: '라벨<탭 또는 2칸+>' (값이 비었거나 밑줄/빈칸)
  if (/^[가-힣A-Za-z][^\t\n]{0,38}(?:\t|\s{2,})\s*[_\s]*$/.test(s)) {
    return { hit: true, via: "라벨" };
  }
  return { hit: false, via: null };
}

/** 칸이름·안내문구로 PSST 섹션 매핑(결정적, 첫 매칭). */
function psst매핑(칸이름: string, 안내문구?: string): { key: PsstKey; 이름: string } | undefined {
  const hay = `${칸이름} ${안내문구 ?? ""}`;
  for (const r of PSST_규칙) {
    if (r.키워드.test(hay)) return { key: r.key, 이름: r.이름 };
  }
  return undefined;
}

/**
 * 칸 유형 판정(결정적, 우선순위: 자금표 > 표 > 체크 > 서술 > 기타).
 * - via가 '마커'(□)면서 짧은 라벨은 '체크'(체크박스 칸) 가능성 — 단, 표/서술 키워드가 있으면 그쪽 우선.
 * - PSST 섹션(문제인식·실현가능성·성장전략·팀)은 본문 서술 칸이므로 '서술'로 본다
 *   (단, 자금표·표 키워드가 있으면 그쪽이 우선 — 예: '팀 구성표'는 표).
 */
function 유형판정(
  칸이름: string,
   안내문구: string | undefined,
  via: "번호" | "마커" | "라벨" | null,
  isPsst: boolean
): 필드유형 {
  const hay = `${칸이름} ${안내문구 ?? ""}`;

  if (자금표_규칙.test(hay)) return "자금표";
  if (표_규칙.test(hay)) return "표";
  // 체크박스: □ 마커 + 짧은 선택지 라벨(표/서술 키워드 없음, PSST 섹션 아님)
  if (via === "마커" && !isPsst && !서술_규칙.test(hay) && !표_규칙.test(hay) && 칸이름.length <= 20) {
    return "체크";
  }
  // PSST 섹션 헤더(팀 구성 포함)는 본문 서술 칸.
  if (isPsst) return "서술";
  if (서술_규칙.test(hay)) return "서술";
  return "기타";
}

/**
 * 칸에 들어갈 '사실'을 묻는 자연어 질문 생성(결정적).
 * required.ts 질문 스타일: "이 칸에 무엇을(사실) 넣을지"를 구체적으로 묻고, 안내문구가 있으면 반영.
 * 창업자의 답을 지어내지 않고 "묻기만" 한다.
 */
function 질문생성(칸이름: string, 유형: 필드유형, 안내문구: string | undefined, psst?: { 이름: string }): string {
  const 이름 = 칸이름 || "이 항목";
  const 안내 = 안내문구 ? ` (서식 안내: ${안내문구})` : "";

  switch (유형) {
    case "자금표":
      return `'${이름}' 칸에 들어갈 자금집행 내역을 '비목 / 산출근거(단가×수량) / 금액'으로 알려주세요. 합계(SUM)가 정확히 맞아야 합니다.${안내}`;
    case "표":
      return `'${이름}' 표의 각 칸에 들어갈 사실(항목별 값)을 알려주세요. 빈칸 없이 정확한 수치·명칭으로요.${안내}`;
    case "체크":
      return `'${이름}' 항목에 해당하는지(체크 여부)와 근거를 알려주세요.${안내}`;
    case "서술": {
      const 힌트 = psst ? ` — PSST '${psst.이름}' 섹션에 해당합니다` : "";
      return `'${이름}' 칸에 들어갈 내용을 사실(수치·실적·기관명) 중심으로 알려주세요${힌트}. 정성적 표현 대신 근거로요.${안내}`;
    }
    default:
      return `'${이름}' 칸에 들어갈 내용(사실)을 알려주세요.${안내}`;
  }
}

// ── 공개 API ─────────────────────────────────────────────────────────────────

/**
 * 정부 사업계획서 서식 텍스트를 분석해 필드 목록·질문·감지요약을 반환한다.
 * - 필드 순서는 서식 등장 순서 그대로(재배열 금지).
 * - 서식으로 보이지 않으면(80자 미만 또는 필드 2개 미만) 정중한 '오류' 재요청을 반환한다.
 */
export function analyzeFormText(input: FormAnalysisInput): FormAnalysis {
  const grant_id = input.grant_id;
  const raw = input.form_text ?? "";
  const text = String(raw);

  // ── 서식 검증 1: 길이 ──
  if (text.replace(/\s+/g, "").length < 최소길이) {
    return {
      ...(grant_id ? { grant_id } : {}),
      오류:
        `서식으로 보이지 않습니다(내용이 ${최소길이}자 미만). ` +
        "공고에서 내려받은 사업계획서 서식(예: 일반현황 표, 1.문제인식~4.팀 구성, 자금집행계획 표)의 " +
        "칸·항목이 보이도록 전체를 붙여넣어 주세요.",
      필드목록: [],
      감지요약: "서식 미인식(길이 부족)",
      입력필요: [],
      고지: 작성고지,
    };
  }

  // ── 필드 추출(줄 단위, 등장 순서 보존) ──
  const lines = text.split(/\r?\n/);
  const 필드목록: FormField[] = [];
  const 입력필요: string[] = [];
  const seen = new Set<string>(); // 중복 칸이름 방지(동일 라벨 반복 시 최초만)

  for (const line of lines) {
    const { hit, via } = 필드헤더인가(line);
    if (!hit) continue;

    const { 칸이름, 안내문구 } = 라벨분리(line);
    const 정규이름 = 원문자정규화(칸이름).replace(/\s+/g, " ").trim();
    if (!정규이름) continue; // 라벨이 비면 필드 아님

    // 중복 칸이름은 한 번만(서식 구분선·반복 헤더 방지). 대소문자·공백 무시 키.
    const key = 정규이름.replace(/\s+/g, "").toLowerCase();
    if (seen.has(key)) continue;
    seen.add(key);

    const psst = psst매핑(정규이름, 안내문구);
    const 유형 = 유형판정(정규이름, 안내문구, via, !!psst);
    const 질문 = 질문생성(정규이름, 유형, 안내문구, psst);

    const field: FormField = {
      순번: 필드목록.length + 1,
      칸이름: 정규이름,
      유형,
      질문,
      ...(안내문구 ? { 안내문구 } : {}),
      ...(psst ? { psst매핑: psst.key } : {}),
    };
    필드목록.push(field);
    입력필요.push(입력필요표기(`${정규이름} — 서식 칸에 들어갈 사실`));
  }

  // ── 서식 검증 2: 필드 수 ──
  if (필드목록.length < 최소필드수) {
    return {
      ...(grant_id ? { grant_id } : {}),
      오류:
        `서식으로 보이지 않습니다(인식된 칸이 ${필드목록.length}개, 최소 ${최소필드수}개 필요). ` +
        "번호 항목(1. / ①), 섹션 마커(□), 표 라벨(예: '기업명:'), PSST 섹션명(문제인식·실현가능성 등)이 " +
        "포함된 실제 서식을 붙여넣어 주세요. 일반 산문·설명글은 서식이 아닙니다.",
      필드목록: [],
      감지요약: `서식 미인식(칸 ${필드목록.length}개 < 최소 ${최소필드수}개)`,
      입력필요: [],
      고지: 작성고지,
    };
  }

  // ── 감지 요약(결정적) ──
  const 유형별: Record<필드유형, number> = { 표: 0, 서술: 0, 자금표: 0, 체크: 0, 기타: 0 };
  let psst수 = 0;
  for (const f of 필드목록) {
    유형별[f.유형] += 1;
    if (f.psst매핑) psst수 += 1;
  }
  const 유형요약 = (["표", "서술", "자금표", "체크", "기타"] as 필드유형[])
    .filter((t) => 유형별[t] > 0)
    .map((t) => `${t} ${유형별[t]}`)
    .join(", ");
  const 감지요약 =
    `필드 ${필드목록.length}개 감지 (${유형요약})` +
    (psst수 > 0 ? ` · PSST 매핑 ${psst수}개` : "") +
    (유형별.자금표 > 0 ? " · 자금집행표 포함" : "");

  return {
    ...(grant_id ? { grant_id } : {}),
    필드목록,
    감지요약,
    입력필요,
    고지: 작성고지,
  };
}
