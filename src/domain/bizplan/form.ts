/**
 * 서식 분석 — analyzeFormText()
 * 창업자가 붙여넣은 "정부 사업계획서 서식 텍스트"를 결정적으로 파싱해,
 * 각 칸(필드)이 무엇을 요구하는지 구조화하고 "이 칸에 들어갈 사실"을 묻는 질문으로 변환한다.
 *
 * 출처(프레임워크): 정부지원 사업계획서 표준 서식 구조(knowledge.ts §9)
 *   — 일반현황 표 / PSST 본문(문제인식·실현가능성·성장전략·팀) / 자금집행계획 표.
 *
 * 결정성(최우선): Math.random 금지. 같은 입력 → 같은 출력(필드 순서·질문 동일).
 * 사실 무결성(최우선):
 * - 이 엔진은 "서식이 무엇을 묻는지"만 분석한다. 창업자의 답(수치·실적·기관명)을 절대 지어내지 않는다.
 * - 서식이 요구하나 창업자가 채워야 하는 값은 결과의 '입력필요'에 "[입력 필요: ___]"로 표시한다.
 * - 서식으로 보이지 않으면 값을 억지로 뽑지 않고 '오류'로 정중히 재요청한다.
 *
 * 필드 순서 불변: 필드 목록은 항상 서식의 "등장 순서 그대로"다(PSST 순서로 재배열하지 않는다).
 */
import type { PsstKey } from "./types.js";
import { 작성고지 } from "../disclaimer.js";

