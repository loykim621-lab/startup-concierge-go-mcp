/**
 * 페이지 추정 문자열을 만든다.
 * 가정(§10)을 명시해 사용자가 오해하지 않도록 한다.
 */
function 페이지추정문자열(
  현재글자수: number,
  목표페이지: number
): { 추정문: string; 판정: "초과" | "부족" | "적정" } {
  const 최소예상페이지 = 현재글자수 / 페이지당글자수_최대;
  const 최대예상페이지 = 현재글자수 / 페이지당글자수_최소;
  const 중간예상페이지 = 현재글자수 / 페이지당글자수_중간;

  // 소수점 한 자리
  const f = (n: number) => n.toFixed(1);

  const 추정문 =
    `현재 ${현재글자수.toLocaleString()}자 기준 예상 페이지: ` +
    `약 ${f(최소예상페이지)}~${f(최대예상페이지)}p ` +
    `(중간 추정 ${f(중간예상페이지)}p). ` +
    `[가정: 한글 A4 본문 ${페이지당글자수_최소}~${페이지당글자수_최대}자/페이지. ` +
    `실제 값은 폰트 크기·여백·도식 비중에 따라 달라집니다.]`;

  // 중간 추정 기준으로 초과/부족 판정
  let 판정: "초과" | "부족" | "적정";
  const 허용범위 = 0.3; // ±0.3p는 적정으로 판단
  if (중간예상페이지 > 목표페이지 + 허용범위) {
    판정 = "초과";
  } else if (중간예상페이지 < 목표페이지 - 허용범위) {
    판정 = "부족";
  } else {
    판정 = "적정";
  }

  return { 추정문, 판정 };
}

/**
 * 섹션별 글자수 진단 문자열.
 * 섹션별 입력이 있으면 각 섹션의 예상 페이지를 추가로 표시한다.
 */
function 섹션진단(섹션별글자수: Record<string, number>): string {
  const lines: string[] = [];
  for (const [섹션, 글자수] of Object.entries(섹션별글자수)) {
    const 예상 = (글자수 / 페이지당글자수_중간).toFixed(1);
    lines.push(`  • ${섹션}: ${글자수.toLocaleString()}자 → 약 ${예상}p`);
  }
  return lines.length > 0 ? `\n섹션별 추정:\n${lines.join("\n")}` : "";
}

/**
 * HWP 레이아웃 가이드 생성.
 * - 페이지 추정(§10 가정 명시)
 * - 조정 제안(초과/부족/적정)
 * - 단축키표(knowledge.ts HWP_SHORTCUTS)
 * - 가독성 원칙(knowledge.ts HWP_가독성원칙)
 * - 고지(disclaimer.ts 작성고지)
 */
export function buildHwpLayout(input: HwpLayoutInput): HwpLayoutResult {
  const 목표 = input.목표페이지 ?? 10; // 미입력 시 기본 10페이지(일반 예창패)
  const 현재 = input.현재글자수 ?? 0;

  // 페이지 추정
  let 페이지추정 = "";
  let 판정: "초과" | "부족" | "적정" = "부족";

  if (현재 > 0) {
    const 추정 = 페이지추정문자열(현재, 목표);
    페이지추정 = 추정.추정문;
    if (input.섹션별글자수) {
      페이지추정 += 섹션진단(input.섹션별글자수);
    }
    판정 = 추정.판정;
  } else {
    페이지추정 =
      "현재 글자수가 입력되지 않아 페이지 추정 불가. " +
      `목표: ${목표}p. ` +
      `[가정: 한글 A4 본문 ${페이지당글자수_최소}~${페이지당글자수_최대}자/페이지]`;
  }

  // 진단 문자열
  let 진단: string;
  if (현재 === 0) {
    진단 = "글자수 미입력 — 진단 불가. 현재글자수를 입력하면 초과/부족을 진단합니다.";
  } else if (판정 === "초과") {
    const 중간예상 = (현재 / 페이지당글자수_중간).toFixed(1);
    진단 =
      `[분량 초과] 현재 약 ${중간예상}p → 목표 ${목표}p 대비 초과. ` +
      "자간 좁히기·줄간격 줄이기·표 압축으로 줄이고, 표/이미지가 페이지 경계에 걸리지 않도록 배치를 점검하세요.";
  } else if (판정 === "부족") {
    const 중간예상 = (현재 / 페이지당글자수_중간).toFixed(1);
    진단 =
      `[분량 부족] 현재 약 ${중간예상}p → 목표 ${목표}p 대비 부족. ` +
      "단락 상단 핵심 요약(■ 2~3줄) 추가·도식 삽입·자간 넓히기로 채우고, 빈 공간이 생기지 않도록 맞추세요.";
  } else {
    진단 = `[적정] 현재 글자수가 목표 ${목표}p 범위 안에 들어옵니다. 단, 표/이미지 페이지 걸침을 최종 확인하세요.`;
  }

  // 조정 제안 선택(판정 기반)
  let 조정제안: { 문제: string; 조치: string; 단축키?: string }[];
  if (현재 === 0) {
    조정제안 = [
      {
        문제: "글자수 미입력",
        조치: "현재글자수를 입력하면 초과/부족별 맞춤 조정 제안을 드립니다.",
      },
    ];
  } else if (판정 === "초과") {
    조정제안 = 초과_조정제안;
  } else if (판정 === "부족") {
    조정제안 = 부족_조정제안;
  } else {
    // 적정이어도 페이지 걸침 주의 사항은 제공
    조정제안 = [
      {
        문제: "분량은 적정하나 표/이미지 페이지 걸침 가능성",
        조치:
          "표·이미지가 페이지 경계에 걸치지 않도록 배치를 최종 확인하고, " +
          "필요하면 자간·줄간격으로 미세 조정하세요.",
        단축키: "Alt+Shift+W / Alt+Shift+Z",
      },
    ];
  }

  return {
    진단,
    조정제안,
    단축키표: HWP_SHORTCUTS,
    가독성원칙: HWP_가독성원칙,
    페이지추정,
    고지: 작성고지,
  };
}
