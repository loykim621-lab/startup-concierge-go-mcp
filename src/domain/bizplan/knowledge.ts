/** 0점 답변 감지 → 위반 사유 목록 */
export function detectZeroPoint(text: string): string[] {
  const t = (text || "").replace(/\s+/g, " ");
  const out: string[] = [];
  for (const { 패턴, 사유 } of ZERO_POINT_PATTERNS) {
    if (패턴.test(t)) out.push(사유);
  }
  return out;
}

/** 정성적(비수치) 경쟁비교 감지: 수치(숫자/단위)가 없고 형용사만이면 경고 */
export function isQualitative(value: string | number): boolean {
  if (typeof value === "number") return false;
  const v = String(value);
  const has수치 = /\d/.test(v);
  const has정성 = 정성적표현_PATTERNS.some((re) => re.test(v));
  return !has수치 && has정성;
}
