/**
 * MCP 가이드 프롬프트 — 호스트 AI(Claude/카카오)가 창업자를 end-to-end로 안내하도록 스크립트한다.
 * 사용자가 프롬프트를 고르면(슬래시 메뉴 등) 아래 지시문이 대화에 주입되어, AI가 정해진 여정을 따른다.
 *
 * 사실 무결성: 모르는 사실은 절대 지어내지 말고 사용자에게 물어보도록 강제한다.
 */
import type { McpServer } from "@modelcontextprotocol/sdk/server/mcp.js";
import { z } from "zod";

const 공통원칙 = `[반드시 지킬 원칙]
- 사용자의 사실(수치·실적·기관명·날짜)을 절대 임의로 지어내지 마라. 모르면 사용자에게 묻고, 사용자도 모르면 "[입력 필요]"로 남겨라.
- 한 번에 다 묻지 말고, 그 단계에 꼭 필요한 최소한만 묻는다(required_inputs가 알려주는 우선 질문부터).
- 사용자가 준 내용은 그대로 쓰지 말고 도구로 정부지원 양식(PSST)에 맞게 가공한다(■ 요약·해자·수치·0점답변 차단).
- 모든 공고·자격·점수 출력의 출처·기준시점·고지를 사용자에게 그대로 전달한다.
- 목표는 '선정'이다. 감점 요인(0점답변·정성적 표현·근거 부족)을 잡아 다시 쓰게 돕는다.`;

export function registerPrompts(server: McpServer): void {
  // ① 전체 여정
  server.registerPrompt(
    "사업계획서_풀코스",
    {
      title: "사업계획서 풀코스 (공고 찾기 → 작성 → 점검)",
      description:
        "사업 자료를 받아 맞는 공고를 찾고, 고른 공고에 맞춰 PSST 사업계획서를 작성·점검·합본까지 안내합니다.",
      argsSchema: { 사업설명: z.string().optional().describe("사업 아이템·상황을 아는 만큼(선택). 자료를 붙여넣어도 됩니다.") },
    },
    ({ 사업설명 }) => ({
      messages: [
        {
          role: "user",
          content: {
            type: "text",
            text:
              `당신은 정부 창업지원 컨시어지다. 아래 순서로 나(창업자)를 안내하라. 각 단계가 끝나면 다음으로 넘어가도 될지 확인하라.\n\n` +
              `1) 인테이크: 내 사업 자료(아래 또는 첨부/붙여넣기)를 읽고 업종·창업단계(예비/초기/도약)·지역·강점·핵심 키워드를 정리해 한 줄로 확인받아라. 부족하면 1~2개만 질문.\n` +
              `2) 공고 추천: recommend_grants(+필요시 find_grants)로 내 사업에 맞는 공고를 적합도·이유와 함께 5건 이내로 제시하라.\n` +
              `3) 자격 확인: 내가 공고를 고르면 check_eligibility로 자격을 확인하라(결격·업력·지역·새출발기금 예외 등). 부적합/확인필요면 사유와 대안을 알려라.\n` +
              `4) 최소 질문: required_inputs로 그 공고 계획서에 꼭 필요한 정보 목록을 받고, 내가 아직 주지 않은 것만 우선순위대로 물어라(한 번에 몰아 묻지 말 것).\n` +
              `5) 작성: draft_section으로 P→S1→S2→T를 차례로 작성하라. 내가 준 사실만 사용하고, 빠진 건 [입력 필요]로 두고 그때그때 물어라. 0점답변·정성적 표현이 잡히면 고쳐 쓰게 하라.\n` +
              `6) 도식: market_research(시장규모·경쟁), build_roadmap(자금 징검다리)로 필요한 도식을 만들어라.\n` +
              `7) 점검: plan_review와 score_application으로 감점 요인을 잡고 보완하라(합격선 대비).\n` +
              `8) 합본·분량: assemble_plan으로 전체 계획서를 정부양식 순서로 합치고, hwp_layout으로 목표 페이지에 맞춰라. 최종은 내가 검토 후 한글(HWP) 양식에 넣는다고 안내하라.\n\n` +
              공통원칙 +
              (사업설명 ? `\n\n[내 사업 자료]\n${사업설명}` : `\n\n먼저 내 사업에 대해 아는 만큼 알려달라고 물어보며 시작하라.`),
          },
        },
      ],
    })
  );

  // ② 공고 빠른 매칭
  server.registerPrompt(
    "공고_빠른매칭",
    {
      title: "내 사업에 맞는 공고 빠르게 찾기",
      description: "사업 자료/설명을 분석해 적합한 정부 창업지원 공고를 적합도와 함께 추천합니다.",
      argsSchema: { 사업설명: z.string().optional().describe("사업 아이템·지역·단계 등(선택). 자료 붙여넣기 가능.") },
    },
    ({ 사업설명 }) => ({
      messages: [
        {
          role: "user",
          content: {
            type: "text",
            text:
              `내 사업에 맞는 정부 창업지원 공고를 찾아줘.\n` +
              `1) 내 자료에서 업종·단계(예비/초기/도약)·지역·강점·키워드를 정리해 확인.\n` +
              `2) recommend_grants로 적합도·매칭이유와 함께 5건 이내 추천.\n` +
              `3) 관심 공고를 고르면 check_eligibility로 자격까지 확인.\n` +
              `모르는 내 정보는 지어내지 말고 1~2개만 질문해줘. 출처·기준시점·고지는 그대로 보여줘.` +
              (사업설명 ? `\n\n[내 사업 자료]\n${사업설명}` : ``),
          },
        },
      ],
    })
  );
}
