/** 업력요건 원문 텍스트 → 최대 업력(개월) 파싱. 확정 못하면 undefined. */
export function parse업력최대개월(업력요건?: string): number | undefined {
  if (!업력요건) return undefined;
  const t = 업력요건.replace(/\s/g, "");
  if (t.includes("예비창업") || t.includes("예비")) return 0; // 예비=업력 0 전제
  const m = t.match(/(\d+)\s*년/);
  if (m) return parseInt(m[1], 10) * 12;
  const mm = t.match(/(\d+)\s*개?월/);
  if (mm) return parseInt(mm[1], 10);
  return undefined;
}
